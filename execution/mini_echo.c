#include "../parsing/mini.h"

void   ft_echo(char *str, int n)
{
    int i;

    i = 0;
    while (str[i])
    {
        write(1, &str[i], 1);
        i++;
    }
    if (n == 0)
        write(1, "\n", 1);
}