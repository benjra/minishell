#include "../parsing/mini.h"

void    ft_env(char **my_env)
{
    int i;

    i = 0;
    while (my_env[i])
    {
        ft_putstr_fd(my_env[i], 1);
        ft_putchar_fd('\n', 1);
        i++;
    }
}
