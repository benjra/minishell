#include "libft.h"

int	ft_isprint(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57)
		|| (c >= 32 && c <= 126))
	{
		return (1);
	}
	else
		return (0);
}
